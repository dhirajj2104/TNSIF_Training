package com.java.day_six.overloading;

public class multiplication {

	public int Multiplication(int x, int y) {
		return x * y;
	}
	
	public int Multiplication(int x, int y, int z) {
		return x * y * z;
	}
	
	public float Multiplication(float x, float y) {
		return x * y;
	}
	
	public float Multiplication(float x, float y, float z) {
		return x * y *z;
	}
}
